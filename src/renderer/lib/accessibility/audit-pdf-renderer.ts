function renderIssuesBySection(issues: AuditIssue[]): string {
  const severities: AuditSeverity[] = ['critical', 'serious', 'moderate', 'minor']
  let html = ''

  for (const severity of severities) {
    const filtered = issues.filter(i => i.severity === severity)
    if (filtered.length === 0) continue

    html += `<h3 style="color: ${SEVERITY_COLORS[severity]}">${capitalize(severity)} (${filtered.length})</h3>`
    for (const issue of filtered) {
      html += `
        <div class="issue" style="border-left-color: ${SEVERITY_COLORS[severity]}">
          <div class="title">${escapeHtml(issue.title)}</div>
          <div class="meta">${escapeHtml(issue.criterion)} &bull; ${escapeHtml(issue.lessonTitle)}</div>
          <div class="description">${escapeHtml(issue.description)}</div>
          <div class="suggestion">${escapeHtml(issue.suggestion)}</div>
        </div>
      `
    }
  }

  if (issues.length === 0) {
    html += '<p style="color: #16a34a; font-weight: 600">No accessibility issues found. Great job!</p>'
  }

  return html
}

function renderUDLModule(title: string, checklist: UDLChecklist): string {
  const scores = udlPillarScores(checklist)
  const total = udlChecklistScore(checklist)

  return `
    <div class="udl-section">
      <h3>${escapeHtml(title)} — ${total.checked}/${total.total} items</h3>

      <p><strong>Representation</strong> (${scores.representation}%)</p>
      <div class="udl-bar"><div class="udl-bar-fill" style="width: ${scores.representation}%; background: #3b82f6"></div></div>
      ${renderChecklist(checklist.representation, {
        multipleFormats: 'Multiple content formats',
        altTextPresent: 'Alt text on all images',
        transcriptsPresent: 'Transcripts on audio/video',
        captionsPresent: 'Captions on video',
        readingLevelAppropriate: 'Appropriate reading level'
      })}

      <p><strong>Action & Expression</strong> (${scores.action}%)</p>
      <div class="udl-bar"><div class="udl-bar-fill" style="width: ${scores.action}%; background: #8b5cf6"></div></div>
      ${renderChecklist(checklist.action, {
        keyboardNavigable: 'Fully keyboard navigable',
        multipleResponseModes: 'Multiple response modes',
        sufficientTime: 'Sufficient time for tasks'
      })}

      <p><strong>Engagement</strong> (${scores.engagement}%)</p>
      <div class="udl-bar"><div class="udl-bar-fill" style="width: ${scores.engagement}%; background: #f59e0b"></div></div>
      ${renderChecklist(checklist.engagement, {
        choiceAndAutonomy: 'Learner choice and autonomy',
        relevantContext: 'Relevant, meaningful context',
        feedbackPresent: 'Feedback on assessments',
        progressVisible: 'Progress tracking visible'
      })}
    </div>
  `
}

function renderChecklist(data: Record<string, boolean>, labels: Record<string, string>): string {
  return Object.entries(labels).map(([key, label]) => {
    const checked = data[key] ?? false
    return `<div class="check-item"><span class="icon">${checked ? '✅' : '⬜'}</span>${escapeHtml(label)}</div>`
  }).join('')
}

function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
}

function scoreColor(score: number): string {
  if (score >= 80) return '#16a34a'
  if (score >= 50) return '#d97706'
  return '#dc2626'
}

function gradeLevelColor(grade: number | null): string {
  if (grade === null) return '#6b7280'
  if (grade <= 8) return '#16a34a'
  if (grade <= 12) return '#d97706'
  return '#dc2626'
}

function capitalize(str: string): string {
  return str.charAt(0).toUpperCase() + str.slice(1)
}
