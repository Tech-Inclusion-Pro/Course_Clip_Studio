import type { Course, UDLChecklist } from '@/types/course'
import type { AuditReport, AuditIssue, AuditSeverity } from './audit-engine'
import { udlChecklistScore, udlPillarScores } from '@/lib/course-helpers'

const SEVERITY_COLORS: Record<AuditSeverity, string> = {
  critical: '#dc2626',
  serious: '#ea580c',
  moderate: '#d97706',
  minor: '#6b7280'
}

